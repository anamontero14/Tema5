package examen.clases;

/**
 * Interfaz que maneja el subir y bajar el volumen de los mandos o dispositivos
 * que la implementen
 */
public interface Velocidad {

	/**
	 * Método que sirve para subir la velocidad
	 */
	public void subirVelocidad();

	/**
	 * Método que sirve para bajar la velocidad
	 */
	public void bajarVelocidad();

}
