package examen.clases;

/**
 * Interfaz que servirá para subir o bajar el volumen en un mando o en otro
 * dispositivo
 */
public interface Volumen {

	/**
	 * Método que se encargará de subir el volumen
	 */
	public void subirVolumen();

	/**
	 * Método que se encargará de bajar el volumen
	 */
	public void bajarVolumen();
}
